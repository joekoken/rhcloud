					<p class="jbox"><img alt="Blackboard Mobile" src="images/blackboard_Guide/bbmob1(1).png" /></p>
					 
					<p><strong>Blackboard Student</strong></p>
					<p>Bb Student is Blackboard's first mobile app centered around one type of user: students. Get ready for a completely new experience and look for your mobile learning! The app provides an intuitive way for you to interact with courses, content, instructors, and other students. Use it throughout your academic journey to stay on top of coursework and plan ahead.</p>
					<p>An app designed just for you...students only!</p>
					<p>With Bb Student, you can do the following:</p>
					<ul style="margin-left: 40px;">
						<li>Enjoy a modern, easy-to-use design.</li>
						<li>Receive updates about your courses, content, and work due in the activity stream.</li>
						<li>Take assignments, tests, and quizzes right from the app and review your submissions.</li>
						<li>View your course grades as well as individual test and assignment grades.</li>
						<li>Stay on task with a list of all due dates.</li>
						<li>Collaborate with your class through video conferencing on iOS and Android</li>
						<li>Attach assignment files from Google Drive, Dropbox, and OneDrive</li>
					</ul>
