				<h4 class="panel-title"><a aria-controls="Blackboard_Mobile_Student3" aria-expanded="true" class="collapsed" data-toggle="collapse" href="#Blackboard_Mobile_Student3" role="button">Feature Guide</a></h4>
			</div>
			<div aria-expanded="true" aria-labelledby="heading-Blackboard_Mobile_Student3" class="panel-collapse collapse" id="Blackboard_Mobile_Student3" role="tabpanel">
				<div class="panel-body"><strong>Navigation and Notifications</strong>
					<div class="nbox">
						<div class="row kbox" style="color: white; background-color: #186db7; text-align: center;">
							<div class="col-xs-3"><strong>Feature</strong></div>
							<div class="col-xs-9"><strong>Description</strong></div>
						</div>
						<div class="row kbox">
							<div class="col-xs-3"><strong>Navigation</strong></div>
							<div class="col-xs-9">We put your most-used features all in one place. The base navigation gives you easy access to course updates, view course content, and check your grades.<br /> <br /> <strong>Swipe down</strong> to go to a previous screen. When you expand the navigation, <strong>breadcrumbs</strong> appear at the bottom of the screen.</div>
						</div>
						<div class="row kbox">
							<div class="col-xs-3"><strong>Activity Stream</strong></div>
							<div class="col-xs-9">See an up-to-the-minute list of important course content and actions you need to take. The activity stream automatically prioritizes this content to help you focus on the tasks that you want to take care of right away.<br /> <br /> Tap an item in the stream to open it and begin working. Click the linked course title within the item if you want to go to the course outline instead.</div>
						</div>
						<div class="row kbox">
							<div class="col-xs-3"><strong>Due Dates</strong></div>
							<div class="col-xs-9">See upcoming assignments and tests across all of your courses. Work is color coded by course and grouped by day and week. You can quickly see what's due and prioritize your work.<br /> <br /> Note that tests and assignments show in this list only if your instructor specified due dates for them.</div>
						</div>
					</div>
					<br /> <br /> <strong>Courses and People</strong>
					<div class="nbox">
						<div class="row kbox" style="color: white; background-color: #186db7; text-align: center;">
							<div class="col-xs-3"><strong>Feature</strong></div>
							<div class="col-xs-9"><strong>Description</strong></div>
						</div>
						<div class="row kbox">
							<div class="col-xs-3"><strong>Profiles and Tiles</strong></div>
							<div class="col-xs-9">
								<div>Your Bb Student profile is populated with your Blackboard Learn cloud profile information, including your name and avatar. You can easily change your avatar in the app and replace all instances of your avatar in Bb Student and Learn. To learn more about profiles, see<a href="https://en-us.help.blackboard.com/Bb_Student/060_FAQs_-_Features" title="FAQs - Features">FAQs - Features</a>.</div>
							Your profile also includes tiles that show which school or schools you attend. In the future, you will be able to add more tiles yourself.</div>
						</div>
						<div class="row kbox">
							<div class="col-xs-3"><strong>Courses</strong></div>
							<div class="col-xs-9">View all of your courses—past, present, and future. Tap a course to view its content and assignments. To learn more, see <a href="https://en-us.help.blackboard.com/Bb_Student/Course_List" title="Course List">Course List</a>.</div>
						</div>
						<div class="row kbox">
							<div class="col-xs-3"><strong>Course Content</strong></div>
							<div class="col-xs-9">
								<div>Easily scan course materials and access what you need. Tap a piece of content to view its full details.</div>
							You can view Microsoft<sup>®</sup> Word, Excel<sup>®</sup>, PowerPoint<sup>®</sup>, and PDF content that your instructors include in courses.</div>
						</div>
						<div class="row kbox">
							<div class="col-xs-3"><strong>Collaborate</strong></div>
							<div class="col-xs-9">Video conferencing is a fundamental part of Bb Student. In iOS and Android, you can participate in virtual sessions by using Collaborate with the Ultra Experience. To learn more, see <a href="https://en-us.help.blackboard.com/Bb_Student/Collaborate_in_Bb_Student" title="Collaborate in Bb Student">Collaborate in Bb Student</a>.</div>
						</div>
						<div class="row kbox">
							<div class="col-xs-3"><strong>Cloud Content</strong></div>
							<div class="col-xs-9">In assignments, you can attach files from your cloud file storage. You can connect Bb Student to Google Drive, OneDrive, and Dropbox for easy access to your files within the app. To learn more see <a href="https://en-us.help.blackboard.com/Bb_Student/Link_to_Cloud_Storage" title="Link to Cloud Storage">Link to Cloud Storage</a>.</div>
						</div>
					</div>
					<div>*Organizations are not supported at this time.</div>
					<div> </div>
					<div><br /> <strong>Assignments, Tests, and Grades</strong></div>
					<div class="nbox">
						<div class="row kbox" style="color: white; background-color: #186db7; text-align: center;">
							<div class="col-xs-3"><strong>Feature</strong></div>
							<div class="col-xs-9"><strong>Description</strong></div>
						</div>
						<div class="row kbox">
							<div class="col-xs-3"><strong>Assignments</strong></div>
							<div class="col-xs-9">View, complete, and submit assignments right from the app.</div>
						</div>
						<div class="row kbox">
							<div class="col-xs-3"><strong>Tests</strong></div>
							<div class="col-xs-9">Tests with the following question types are taken within Bb Student: Multiple Choice, True / False, Either / Or, Short Answer, and Essay. If tests contain other question types, Bb Student directs you to a web view of Learn to complete the test.</div>
						</div>
						<div class="row kbox">
							<div class="col-xs-3"><strong>Grades</strong></div>
							<div class="col-xs-9">You get real-time access to grades and feedback within an individual course or from the Grades in the base navigation.</div>
						</div>
